import Foundation

public enum SpecialAgentRemindersCommand: String, Codable, Sendable {
    case list = "reminders.list"
    case add = "reminders.add"
}

public enum SpecialAgentReminderStatusFilter: String, Codable, Sendable {
    case incomplete
    case completed
    case all
}

public struct SpecialAgentRemindersListParams: Codable, Sendable, Equatable {
    public var status: SpecialAgentReminderStatusFilter?
    public var limit: Int?

    public init(status: SpecialAgentReminderStatusFilter? = nil, limit: Int? = nil) {
        self.status = status
        self.limit = limit
    }
}

public struct SpecialAgentRemindersAddParams: Codable, Sendable, Equatable {
    public var title: String
    public var dueISO: String?
    public var notes: String?
    public var listId: String?
    public var listName: String?

    public init(
        title: String,
        dueISO: String? = nil,
        notes: String? = nil,
        listId: String? = nil,
        listName: String? = nil)
    {
        self.title = title
        self.dueISO = dueISO
        self.notes = notes
        self.listId = listId
        self.listName = listName
    }
}

public struct SpecialAgentReminderPayload: Codable, Sendable, Equatable {
    public var identifier: String
    public var title: String
    public var dueISO: String?
    public var completed: Bool
    public var listName: String?

    public init(
        identifier: String,
        title: String,
        dueISO: String? = nil,
        completed: Bool,
        listName: String? = nil)
    {
        self.identifier = identifier
        self.title = title
        self.dueISO = dueISO
        self.completed = completed
        self.listName = listName
    }
}

public struct SpecialAgentRemindersListPayload: Codable, Sendable, Equatable {
    public var reminders: [SpecialAgentReminderPayload]

    public init(reminders: [SpecialAgentReminderPayload]) {
        self.reminders = reminders
    }
}

public struct SpecialAgentRemindersAddPayload: Codable, Sendable, Equatable {
    public var reminder: SpecialAgentReminderPayload

    public init(reminder: SpecialAgentReminderPayload) {
        self.reminder = reminder
    }
}
